/* ================================================================== */
/*  Typst compiler initialization                                       */
/* ================================================================== */

let initialized = false;

function ensureInit(): void {
  if (initialized) return;
  $typst.setCompilerInitOptions({
    getModule: () =>
      'https://cdn.jsdelivr.net/npm/@myriaddreamin/typst-ts-web-compiler@0.7.0-rc2/pkg/typst_ts_web_compiler_bg.wasm',
  });
  initialized = true;
}

/* ================================================================== */
/*  Helpers                                                              */
/* ================================================================== */

function formatTime(d: Date): string {
  return d.toISOString().replace('T', ' ').replace('Z', ' UTC');
}

function formatDuration(sec: number): string {
  const h = Math.floor(sec / 3600);
  const m = Math.floor((sec % 3600) / 60);
  const s = Math.floor(sec % 60);
  if (h > 0) return `${h}h ${m}m ${s}s`;
  if (m > 0) return `${m}m ${s}s`;
  return `${s}s`;
}

function formatEcef(xyz: [number, number, number]): string {
  return `${xyz[0].toFixed(4)}, ${xyz[1].toFixed(4)}, ${xyz[2].toFixed(4)} m`;
}

/** Escape special Typst characters */
function esc(s: string): string {
  return s
    .replace(/\\/g, '\\\\')
    .replace(/#/g, '\\#')
    .replace(/\$/g, '\\$')
    .replace(/</g, '\\<')
    .replace(/>/g, '\\>')
    .replace(/@/g, '\\@')
    .replace(/_/g, '\\_')
    .replace(/\*/g, '\\*');
}

/* ================================================================== */
