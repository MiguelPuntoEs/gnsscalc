/**
 * PDF Report generator using Typst (compiled to WASM in the browser).
 * Produces LaTeX-quality typeset documents with proper typography,
 * booktabs-style tables, numbered sections, and embedded SVG charts.
 *
 * The ~10 MB WASM compiler is lazy-loaded from CDN on first use and cached.
 */

import { $typst } from '@myriaddreamin/typst.ts/dist/esm/contrib/snippet.mjs';
import type { ReportData } from '../util/report-types';
