export async function generateReport(data: ReportData): Promise<Blob> {
  ensureInit();

  // Load branding logos
  let hasLogo = false;
  try {
    const logoResp = await fetch('/img/og-light.png');
    if (logoResp.ok) {
      await $typst.mapShadow(
        '/logo.png',
        new Uint8Array(await logoResp.arrayBuffer()),
      );
      hasLogo = true;
    }
  } catch {
    /* logo is optional */
  }

  // Generate print-themed SVG charts
  const encoder = new TextEncoder();
  const chartFiles: string[] = [];

  if (data.allPositions) {
    const svg = renderSkyPlotSvg(
      data.allPositions,
      data.observedPrns,
      5,
      PRINT_THEME,
    );
    await $typst.mapShadow('/skyplot.svg', encoder.encode(svg));
    chartFiles.push('skyplot.svg');
  }

  const cn0Svg = renderCn0TimelineSvg(data.grid, PRINT_THEME);
  if (cn0Svg) {
    await $typst.mapShadow('/cn0.svg', encoder.encode(cn0Svg));
    chartFiles.push('cn0.svg');
  }

  const satSvg = renderSatCountSvg(data.grid, PRINT_THEME);
  if (satSvg) {
    await $typst.mapShadow('/satcount.svg', encoder.encode(satSvg));
    chartFiles.push('satcount.svg');
  }

  // C/N0 distribution
  const cn0DistSvg = renderCn0DistributionSvg(data.grid, PRINT_THEME);
  if (cn0DistSvg) {
    await $typst.mapShadow('/cn0dist.svg', encoder.encode(cn0DistSvg));
    chartFiles.push('cn0dist.svg');
  }

  // Elevation vs C/N0
  if (data.allPositions) {
    const elevCn0Svg = renderElevCn0Svg(
      data.allPositions,
      data.grid,
      PRINT_THEME,
    );
    if (elevCn0Svg) {
      await $typst.mapShadow('/elevcn0.svg', encoder.encode(elevCn0Svg));
      chartFiles.push('elevcn0.svg');
    }
  }

  if (data.qaResult) {
    // Multipath charts
    if (data.allPositions) {
      const mpSvg = renderMultipathElevSvg(
        data.qaResult.multipath,
        data.allPositions,
        null,
        PRINT_THEME,
      );
      if (mpSvg) {
        await $typst.mapShadow('/multipath.svg', encoder.encode(mpSvg));
        chartFiles.push('multipath.svg');
      }
    }

    const mpBarSvg = renderMultipathRmsBarSvg(
      data.qaResult.multipath,
      PRINT_THEME,
    );
    if (mpBarSvg) {
      await $typst.mapShadow('/mp-rmsbar.svg', encoder.encode(mpBarSvg));
      chartFiles.push('mp-rmsbar.svg');
    }

    const mpSatSvg = renderMultipathPerSatSvg(
      data.qaResult.multipath,
      PRINT_THEME,
    );
    if (mpSatSvg) {
      await $typst.mapShadow('/mp-persat.svg', encoder.encode(mpSatSvg));
      chartFiles.push('mp-persat.svg');
    }

    // Cycle slip chart
    const slipSvg = renderCycleSlipRateSvg(
      data.qaResult.cycleSlips,
      PRINT_THEME,
    );
    if (slipSvg) {
      await $typst.mapShadow('/sliprate.svg', encoder.encode(slipSvg));
      chartFiles.push('sliprate.svg');
    }

    // Completeness charts (one per constellation)
    const compSvgs = renderCompletenessBarSvgs(
      data.qaResult.completeness,
      PRINT_THEME,
    );
    for (const c of compSvgs) {
      const name = `comp-${c.system}.svg`;
      await $typst.mapShadow(`/${name}`, encoder.encode(c.svg));
      chartFiles.push(name);
    }
  }

  // Build Typst source and compile
  const typstSource = buildTypstDocument(data, chartFiles, hasLogo);
  const pdfBytes = await $typst.pdf({ mainContent: typstSource });

  // Cleanup virtual filesystem
  for (const name of chartFiles) {
    await $typst.unmapShadow(`/${name}`);
  }
  if (hasLogo) await $typst.unmapShadow('/logo.png');

  if (!pdfBytes) throw new Error('PDF compilation failed');
  return new Blob([pdfBytes.buffer as ArrayBuffer], {
    type: 'application/pdf',
  });
}

export function downloadReport(blob: Blob, filename: string): void {
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  a.click();
  URL.revokeObjectURL(url);
}
